package org.resteasy.model;

import com.wordnik.swagger.annotations.ApiModel;

@ApiModel(value="Person class")
public class Person {
	
	private String name;
	private int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

}
